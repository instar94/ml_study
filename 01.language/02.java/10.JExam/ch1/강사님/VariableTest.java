package a.b.c;

public class VariableTest {

	public static void main(String[] args){
		
		// 데이터는 문자(문자, 문자열), 숫자(정수, 실수)
		// 정수 데이터 사용
		// int : 정수형 데이터를 담는 데이터 타입
		// 데이터를 담는 공간을 우리는 변수라고 부른다. 
		// i : 변수 : 정수형 데이터를 담는 변수 이름
		// 변수에 데이터를 담을 때는 = (대입 연산자) 사용한다. 
		// i = 1 : i 변수에 정수 1을 대입(넣는다, 초기화)한다.
		// ; : 세미콜론 : 문장을 종결한다.
		int i = 1;
		// int 형 데이터 타입을 사용하려고 선언하고 변수명을 i 로 선언하고 
		// i 변수명에 값 1을 대입(넣는다, 초기화)한다.

		// int 형 변수 i 를 호출(invoke 한다)해서 콘솔에 출력을 한다.
		System.out.println(i);
		

	} // end of main 함수 

} // end of VariableTest 클래스
